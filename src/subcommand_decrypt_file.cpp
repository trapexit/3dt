static
void
_decrypt(void *buf_,
         u64   buflen_)
{
  u32 *p;
  u32 *q;
  u32 part_a;
  u32 part_b;
  u32 mask_1;
  u32 mask_2;

  q = (u32*)&PUBLIC_SIGNATURES_ARRAY;
  mask_1 = DECRYPT_MASK_1;
  mask_2 = DECRYPT_MASK_2;

  p = (u32*)buf_;
  for(u64 i = 0; i < buflen_ / 4; i++)
    {
      if((i % (sizeof(rsa512_sig_t) / 4)) == 0)
        q = (u32*)PUBLIC_THDOKEY_MOD;
      part_a = (((*p ^ *q) & mask_1) >> 4);
      part_b = (((*p ^ *q) & mask_2) << 4);
      *p++ = (part_a | part_b);
      q++;
    }
}

static
void
_decrypt_file(std::fstream &fs_)

{
  std::streamsize filesize;
  std::vector<char> data;

  fs_.seekg(0,std::ios::end);
  filesize = fs_.tellg();
  fs_.seekg(0,std::ios::beg);

  data.resize(filesize);
  
  fs_.read(data.data(),filesize);

  _decrypt((void*)data.data(),data.size());

  fs_.seekp(0,std::ios::beg);
  fs_.write((const char*)data.data(),data.size());
}

void
Subcommand::decrypt_file(const Options::DecryptFile &opts_)
{
  for(const auto &filepath : opts_.filepaths)
    {
      std::fstream fs;

      fs.open(filepath,std::ios::binary|std::ios::in|std::ios::out);
      if(!fs.is_open())
        {
          fmt::print(stderr,"3dt: error opening {}\n",filepath);
          continue;
        }

      ::_decrypt_file(fs);

      fs.close();
    }
}
